package mff.webcrawler.Enums;

/**
 * Enumeration representing the status of a web crawling process.
 * The status indicates the current state or outcome of the crawling execution.
 */
public enum Status {

    /**
     * Indicates that the crawling process is currently in progress.
     */
    IN_PROGRESS,

    /**
     * Indicates that the crawling process has completed successfully.
     */
    COMPLETED,

    /**
     * Indicates that the crawling process has failed.
     */
    FAILED
}
