package mff.webcrawler.Enums;

/**
 * Enumeration representing the frequency at which a web crawling process should occur.
 * The periodicity defines how often the crawler should revisit a website to update its data.
 */
public enum Periodicity {

    /**
     * Represents a periodicity of one minute.
     */
    Minute,

    /**
     * Represents a periodicity of one hour.
     */
    Hour,

    /**
     * Represents a periodicity of one day.
     */
    Day

}
